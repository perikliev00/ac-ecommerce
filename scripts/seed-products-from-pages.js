function normalizeProduct(p) {
  const moshtnost = p.moshtnost != null ? Number(p.moshtnost) : 0;
  const img = productImageSlug(p.brand, p.model, moshtnost) + '.webp';
  return {
    currentOffer: p.currentOffer ?? '',
    category: p.category || 'inverter',
    price: Number(p.price),
    description: p.description || '',
    moshtnost,
    class: p.class || '',
    img,
    brand: p.brand || '',
    model: p.model || '',
    availability: p.availability || 'in_stock',
    energyClass: p.energyClass || '',
    wifi: !!p.wifi,
    roomSize: p.roomSize != null ? Number(p.roomSize) : null,
    recommended: !!p.recommended,
  };
}

async function seed() {
  await mongoose.connect(MONGO_URI);
  let created = 0;
  let skipped = 0;
  for (const p of products) {
    const existing = await Product.findOne({ brand: p.brand, model: p.model });
    if (existing) {
      console.log('Skip (exists): %s %s', p.brand, p.model);
      skipped++;
      continue;
    }
    const doc = new Product(normalizeProduct(p));
    await doc.save();
    console.log('Created: %s %s (id: %s)', doc.brand, doc.model, doc._id);
    created++;
  }
  console.log('Done. Created: %s, Skipped: %s', created, skipped);
  await mongoose.disconnect();
}

if (require.main === module) {
  seed().catch((err) => {
    console.error(err);
    process.exit(1);
  });
}

